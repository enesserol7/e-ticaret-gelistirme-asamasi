			<h3>SIKCA SORULAN SORULAR</h3>
			<h5>Hangi baskı tekniğini kullanıyorsunuz?</h5>
			<p>Bidolu, matbaa işinizin en kaliteli,  istenilen sürede ve uygun fiyata üretilmesi için, ofset baskı, dijital baskı ve organize matbaacılık tekniklerinin hepsini kullanmaktadır.</p>
			<h5>Ürünlerin kalitesinden nasıl emin olacağım?</h5>
			<p>Bidolu, Türkiye'de 81 ile binlerce gönderi yapmıştır. Satın almadan önce baskı kalitemizi görmeniz için ücretsiz olarak numune talep edebilmenizi sağladık. Bu formu doldurarak Numune Kutusunu ücretsiz talep edebilirsiniz.</p>
			<h5>Tasarım hizmeti veriyor musunuz?</h5>
			<p>Bidolu tasarım hizmeti vermemektedir. Hazır tasarımlarınızı kaliteli ve uygun fiyatlarla matbaa baskı ürünlerine çevirir.</p>
			<h5>Baskı öncesi herhangi bir kontrol yapıyor musunuz?</h5>
			<p>Gönderdiğiniz tasarımlar grafikerlerimiz tarafından çözünürlük, yerleşim, kesim payı gibi konularda baskıya uygunluğu açısından kontrol edilir ve gerekli ise düzeltmeler yapılır. Bu işlem sonrasında son bir defa daha kontrol etmeniz için baskı onayı e-postası gönderilir. Siz onay verdikten sonra üretime alınır.</p>
			<h5>Siparişim ne zaman elime ulaşır?</h5>
			<p>Üretim süreleri ürünlere göre değişmektedir. Her bir ürünün üretim süresi bilgisini ürünün kendi sayfasında görebilirsiniz. Baskı onayınız ardından ürünler üretime alınmakta ve belirtilen üretim süreleri sonunda kargoya verilmektedir.</p>
			<h5>Siparişimi Bidolu merkezinden alabilir miyim?</h5>
			<p>Hayır, bütün siparişler kargo ile teslim edilmektedir.</p>
			<h5>Siparişler Bidolu faturası üzerinde olmadan istediğim adrese teslim edilebilir mi?</h5>
			<p>Evet. Temsilcimize isteğinizi ilettiğiniz taktirde, siparişiniz istediğiniz adrese, Bidolu ibaresi olmadan teslim edilebilir.</p>
			<h5>Baskıda herhangi bir sorun olursa iade yapılabiliyor mu?</h5>
			<p>Bidolu oluşabilecek renk farklılığı, kayma gibi baskı sürecinde oluşabilecek her türlü hatada ürün iadesi kabul eder, ürünü yeniden üretir veya sipariş tutarını geri öder. Tasarım hataları ve metinlerde oluşabilecek eksiklikler ve hatalardan sorumlu değildir.</p>
